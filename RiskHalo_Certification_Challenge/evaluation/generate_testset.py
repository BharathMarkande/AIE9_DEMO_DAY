import chromadb
from datasets import Dataset
from evaluation.personas import TRADER_PERSONAS


class RiskHaloTestsetGenerator:
    """
    Simplified Testset Generator for MVP.

    Steps:
    1. Fetch all session summaries from ChromaDB
    2. Concatenate into single raw_doc
    3. Generate 10 persona-based questions (2 per behavioral_state)
    """

    def __init__(
        self,
        collection_name: str = "riskhalo_sessions",
        persist_directory: str = "./chroma_db",
    ):
        self.client = chromadb.PersistentClient(path=persist_directory)
        self.collection = self.client.get_or_create_collection(collection_name)

    # --------------------------------------------------
    # Create Enriched Concatenated Raw Document
    # --------------------------------------------------
    def generate_raw_document(self) -> str:
        """
        Fetches all session summaries from ChromaDB,
        enriches each with structured metadata headers,
        and concatenates them into a single raw document.

        The document contains:
        - Global header explaining data source
        - Chronologically ordered weekly sections
        - Behavioral state and key metrics per session

        Returns:
            str: Structured historical performance record
        """

        data = self.collection.get(include=["documents", "metadatas"])

        documents = data.get("documents", [])
        metadatas = data.get("metadatas", [])

        # Flatten nested list structure if needed
        if documents and isinstance(documents[0], list):
            documents = documents[0]

        # Combine documents + metadata
        sessions = list(zip(documents, metadatas))

        # --------------------------------------------------
        # Global Header
        # --------------------------------------------------
        global_header = (
            "RISKHALO HISTORICAL PERFORMANCE RECORD\n"
            "This document contains concatenated weekly behavioral "
            "performance summaries generated by the RiskHalo system.\n\n"
            "Each section represents one completed weekly trading analysis.\n"
            "Sessions are ordered chronologically.\n"
            "Metrics include behavioral classification, severity score, "
            "and overall discipline score.\n\n"
            "==================================================\n"
        )

        enriched_blocks = []

        for index, (doc, meta) in enumerate(sessions, start=1):

            session_id = meta.get("session_id", "UNKNOWN")
            behavioral_state = meta.get("behavioral_state", "UNKNOWN")
            severity_score = meta.get("severity_score", "N/A")
            discipline_score = meta.get("overall_discipline_score", "N/A")

            block_header = (
                f"WEEK {index}\n"
                f"Session ID: {session_id}\n"
                f"Behavioral State: {behavioral_state}\n"
                f"Severity Score: {severity_score}\n"
                f"Discipline Score: {discipline_score}\n\n"
            )

            enriched_block = (
                "==================================================\n"
                + block_header
                + doc.strip()
                + "\n\n"
            )

            enriched_blocks.append(enriched_block)

        raw_doc = global_header + "\n".join(enriched_blocks)

        print(f"raw_doc ========= {raw_doc}")
        return raw_doc

    # --------------------------------------------------
    # Generate 10 Questions (2 per state)
    # --------------------------------------------------
    def generate_questions(self):
        """
        Select first 2 questions from each persona.
        Assumes 5 behavioral states defined in TRADER_PERSONAS.
        """

        questions = []

        for persona_name, persona in TRADER_PERSONAS.items():
            persona_questions = persona["question_templates"][:2]
            questions.extend(persona_questions)

        return questions  # total = 10

    # --------------------------------------------------
    # Build Dataset
    # --------------------------------------------------
    def generate_dataset(self):
        """
        Builds final dataset:
        - 10 questions
        - Same raw_doc as context
        """

        raw_doc = self.generate_raw_document()
        questions = self.generate_questions()

        rows = []

        for question in questions:
            rows.append(
                {
                    "question": question,
                    "context": raw_doc,
                }
            )

        print(f"generate_dataset::rows length: {len(rows)}")
        return Dataset.from_list(rows)